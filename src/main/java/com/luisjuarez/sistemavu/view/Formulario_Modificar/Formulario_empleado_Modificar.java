    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Empleado empleado = new Empleado();

// Validate nombre
        String nombre = jTextField1.getText();
        if (nombre.isEmpty() || !nombre.matches("^[a-zA-Z]+$")) {
            JOptionPane.showMessageDialog(null, "Nombre no puede estar vac√≠o y no debe contener n√∫meros");
            return;
        }
        empleado.setNombre(nombre);

// Validate apellido
        String apellido = jTextField2.getText();
        if (apellido.isEmpty() || !apellido.matches("^[a-zA-Z]+$")) {
            JOptionPane.showMessageDialog(null, "Apellido no puede estar vac√≠o y no debe contener n√∫meros");
            return;
        }
        empleado.setApellido(apellido);

// Validate usuario
        String usuario = jTextField3.getText();
        if (usuario.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Usuario no puede estar vac√≠o");
            return;
        }
        empleado.setUsuario(usuario);

// Validate contrasena
        String contrasena = jTextField4.getText();
        if (contrasena.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Contrase√±a no puede estar vac√≠a");
            return;
        }
        empleado.setContrasena(contrasena);

// Validate correo
        String correo = jTextField5.getText();
        if (correo.isEmpty() || !(correo.endsWith("@gmail.com") || correo.endsWith("@hotmail.com"))) {
            JOptionPane.showMessageDialog(null, "Correo no puede estar vac√≠o y debe tener el formato @gmail.com o @hotmail.com");
            return;
        }
        empleado.setCorreo(correo);

// Register the employee
        SistemaPrincipal.getEmpleadoService().registrarEmpleado(empleado);

// Notify successful creation
        JOptionPane.showMessageDialog(null, "Empleado creado exitosamente");

        try {
            SistemaPrincipal.getEmpleadoService().cargarTabla(TableEmpleado);
        } catch (SQLException ex) {
            Logger.getLogger(Formulario_empleado_Modificar.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        this.dispose();
         
    }//GEN-LAST:event_jButton1ActionPerformed

